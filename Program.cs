    class Program
    {
        // Zufallszahlengenerator zum Wuerfeln einer Zahl
        static readonly Random random = new Random();

        // zu verwendende Wortliste
        static string language = "D";

        // Anzahl der Woerter, die das Passwort enthalten soll
        static int countWords = 3;

        // Trennzeichen zwischen Woertern
        static string delimiter = "_";

        // Kennzeichen, ob das Passwort eine Zahl enthalten soll
        static string mustContainDigits = "N";

        // Kennzeichen, ob das Passwort Grossbuchstaben enthalten soll
        static string mustContainUppercaseLetters = "J";

        // Kennzeichen, ob das Passwort Sonderzeichen enthalten soll
        static string mustContainSpecialCharacters = "N";

        // Sonderzeichen, die der Passphrase hinzugefuegt werden sollen
        static string specialCharacters = "!";

        // Main
        [STAThread]
        static void Main(string[] args)
        {
            string input = "";

            // UTF-8 Zeichensatz fuer die Consolen-Ausgabe ermoeglichen
            Console.OutputEncoding = Encoding.UTF8;

            // Erzeugtes Passwort
            StringBuilder password = new StringBuilder();

            Console.WriteLine(@"
--------------------------------------------------------------------------------
                              Passwort-Generator
--------------------------------------------------------------------------------

Das Programm erzeugt Passphrassen nach der 'Diceware'-Methode (dice: würfeln)
(siehe: https://de.wikipedia.org/wiki/Diceware).

Hierbei wird aus einer Wortliste ein langes aber leicht zu merkendes
Passwort erzeugt. Es steht wahlweise eine deutsche und eine englische Wortliste
zur Verfuegung. Die Wortlisten sind entnommen aus:
- https://raw.githubusercontent.com/dys2p/wordlists-de/main/de-1296-v1-diceware.txt
- https://www.eff.org/files/2016/09/08/eff_short_wordlist_1.txt

Die Anzahl der Worte in der Passphrase können variiert werden, ebenso
die Verwendng von Trennzeichen, Sonderzeichen, Zahlen usw..

Das Programm fragt die Optionen vom Benutzer ab. Hierbei sind Standardwerte gesetzt,
die durch eine Eingabe überschrieben werden können.

Die erzeugte Passphrase wird auf dem Bildschirm angezeigt und gleichzeitig in die
Zwischenablage kopiert, so dass sie dort zum Einfügen zur Verfügung steht.
");
            //
            // Parameter abfragen
            //

            // Sprache
            do
            {
                //Console.Write("Soll die englische oder deutsche Wortliste verwendet werden (D/E) [{0}]? ", language);
                Console.Write("Wortliste (d)eutsch (e)nglisch (d/e) [{0}]? ", language);
                input = Console.ReadLine();
                if (input.Length > 0)
                {
                    input = input.ToUpper();
                    if (!"DE".Contains(input))
                    {
                        Console.WriteLine("Ungültige Eingabe!");
                        continue;
                    }
                    language = input;
                }
                break;
            } while (true);


            // Anzahl Woerter
            do
            {
                //Console.Write("Anzahl der Wörter, die in der Passphrase enthalten sein sollen [{0}]: ", countWords);
                Console.Write("Anzahl Wörter [{0}]: ", countWords);
                input = Console.ReadLine();
                if (input.Length > 0)
                {
                    if (!int.TryParse(input, out countWords))
                    {
                        Console.WriteLine("Ungültige Zahl!");
                        continue;
                    }
                    if (countWords < 1)
                    {
                        Console.WriteLine("Anzahl kann nicht '{0}' sein!", countWords);
                        continue;
                    }
                }
                break;
            } while (true);

            // Trennzeichen zwischen Woertern
            if (countWords > 1)
            {
                //Console.Write("Trennzeichen zwischen den Wörtern ['{0}']: ", delimiter);
                Console.Write("Trennzeichen ['{0}']: ", delimiter);
                input = Console.ReadLine();
                if (input.Length > 0)
                {
                    delimiter = input;
                }
            }

            // Grossbuchtaben
            do
            {
                Console.Write("Großbuchstaben (J/N) [{0}]? ", mustContainUppercaseLetters);
                input = Console.ReadLine();
                if (input.Length > 0)
                {
                    input = input.ToUpper();
                    if (!"JN".Contains(input))
                    {
                        Console.WriteLine("Ungültige Eingabe!");
                        continue;
                    }
                    mustContainUppercaseLetters = input;
                }
                break;
            } while (true);

            // Zahlen
            do
            {
                Console.Write("Zahlen (J/N) [{0}]? ", mustContainDigits);
                input = Console.ReadLine();
                if (input.Length > 0)
                {
                    input = input.ToUpper();
                    if (!"JN".Contains(input))
                    {
                        Console.WriteLine("Ungültige Eingabe!");
                        continue;
                    }
                    mustContainDigits = input;
                }
                break;
            } while (true);

            // Sonderzeichen J/N
            do
            {
                Console.Write("Sonderzeichen (J/N) [{0}]? ", mustContainSpecialCharacters);
                input = Console.ReadLine();
                if (input.Length > 0)
                {
                    input = input.ToUpper();
                    if (!"JN".Contains(input))
                    {
                        Console.WriteLine("Ungültige Eingabe!");
                        continue;
                    }
                    mustContainSpecialCharacters = input;
                }
                break;
            } while (true);

            // Eingabe Sonderzeichen
            if (mustContainSpecialCharacters[0] == 'J')
            {
                Console.Write("Sonderzeichen: ['{0}']: ", specialCharacters);
                input = Console.ReadLine();
                if (input.Length > 0)
                {
                    specialCharacters = input;
                }
            }

            // das Passwort soll sich aus 3 Woertern zusammensetzen
            for (int i = 0; i < countWords; i++)
            {
                // Wort aus der Wortliste wuerfeln
                string word = diceWord();

                // wenn das Passwort eine Zahl enthalten soll, wird diese an das erste
                // Wort angefuegt
                if ((mustContainDigits[0] == 'J') && (i == 0))
                {
                    int n = random.Next(1, 9999);
                    word = String.Format("{0}{1}", word, n);
                }

                // wenn das Passwort Grossbuchstaben enthalten soll, das letzte Wort in
                // Grossbuchstaben umwandeln. Wenn die Passphrase nur aus einem Wort
                // besteht, dann den ersten Buchstaben in einen Grossbuchtaben umwandeln
                if (mustContainUppercaseLetters[0] == 'J')
                {
                    if (countWords == 1)
                    {
                        word = word.Substring(0, 1).ToUpper() + word.Substring(1);
                    }
                    else if (i == (countWords - 1))
                    {
                        word = word.ToUpper();
                    }
                }

                // Unterstrich als Trannzeichen einfuegen
                if (password.Length > 0)
                    password.Append(delimiter);
                password.Append(word);
            } // for

            // wenn das Passwort Sonderzeichen enthalten soll, werden diese 
            // am Ende angefuegt
            if (mustContainSpecialCharacters[0] == 'J')
            {
                password.Append(specialCharacters);
            }

            // fertig
            Clipboard.SetText(password.ToString());
            Console.Write("Passphrase: ");
            Console.ForegroundColor = ConsoleColor.Yellow;
            Console.WriteLine("{0}", password.ToString());
            Console.ForegroundColor = ConsoleColor.DarkGray;
            Console.WriteLine("Die Passphrase wurde in die Zwischenablage kopiert.");
            Console.ResetColor();
        }

        // Wuerfelt ein Wort aus der Wortliste
        static string diceWord()
        {
            // 5 Ziffern fuer das naechste Wort aus der Wortliste wuerfeln
            string wordnumber = dice();

            // Wort aus der Wortliste ermitteln
            using (StringReader reader = new StringReader(language[0] == 'D' ? wordListDE : wordListEN))
            {
                // naechste Zeile aus der Wortliste lesen
                string line = reader.ReadLine();
                while (line != null)
                {
                    // wenn die Zeile mit der Wortnummer beginnt, das Wort aus der Zeile
                    // extrahieren und an dem Passwort-String anfuegen
                    if (line.StartsWith(wordnumber))
                        return line.Substring(8);
                    // naechste Zeile aus der Wortliste lesen
                    line = reader.ReadLine();
                }
            }
            return "";
        }

        // Wuerfelt eine Ziffernfolge aus 4 Ziffern
        static string dice()
        {
            char[] diced = new char[4];
            for (int i = 0; i < 4; i++)
                diced[i] = (char)(random.Next(1, 6) + 48);
            return new string(diced);
        }



